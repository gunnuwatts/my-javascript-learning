//Arithmetic operators
let a = 5;
let b = 2;

console.log("a = ", a, " & b = ", b);
console.log("a + b =", a + b);
console.log("a - b =", a - b);
console.log("a * b =", a * b);
console.log("a / b =", a / b);
console.log("a % b =", a % b);    //modulus operator(remainder)
console.log("a ** b =", a ** b);    //exponentiation(5^2)